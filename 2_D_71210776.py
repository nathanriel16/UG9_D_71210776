#menghitung rata-rata uang saku anak
anakpertama = int(input("Uang saku anak pertama:"))
anakkedua = int(input("Uang saku anak kedua:"))
anakketiga = int(input("Uang saku anak ketiga:"))

#proses
ratarata = (anakpertama+anakkedua+anakketiga)/3

#print
print("Rata-rata dari ", anakpertama, "+", anakkedua, "+", anakketiga, " adalah", ratarata)
